### CRUD é um acrônimo para as operações básicas de um sistema de gerenciamento de banco de dados: Criar, Realer, Utualizar e Deletar (Create, Read, Update, Delete em inglês). 
# Essencialmente, o CRUD representa as ações fundamentais que um usuário pode realizar com os dados em um sistema. 
# Em detalhes:
# Criar (Create): Adiciona novos registros ou dados ao banco de dados. 
# Ler (Read): Recupera e exibe dados existentes do banco de dados. 
# Atualizar (Update): Modifica dados existentes no banco de dados. 
# Deletar (Delete): Remove registros ou dados do banco de dados. 
# O CRUD é um conceito fundamental em praticamente todas as aplicações que trabalham com dados, 
# desde sistemas simples até aplicações web complexas. Ele serve como base para a interação 
# com bancos de dados, permitindo que os usuários manipulem as informações de forma eficiente. 